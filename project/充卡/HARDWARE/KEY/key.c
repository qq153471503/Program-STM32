
#include "stm32f10x.h"
#include "key.h"
#include "delay.h"

/***************************************************************
** 功能：     按键硬件初始化
** 参数：	  无参数
** 返回值：   无
****************************************************************/
void KEY_Init(void)
{
  RCC->APB2ENR|=1<<3;    //使能PORTB时钟
	GPIOB->CRH&=0X0000FFFF; 	  
	GPIOB->CRH|=0X88880000; //PB12~PB15设置成输入
	GPIOB->ODR|=0xf000;	    //PB12~PB15设置上拉		   

}
